class SubjectLine(models.Model):
    """
    A single email subject line belonging to a campaign.
    Can be written manually or generated by the AI.
    """

    class Tone(models.TextChoices):
        PROFESSIONAL = "professional", "Professional"
        CASUAL       = "casual",       "Casual"
        URGENT       = "urgent",       "Urgent"
        CURIOUS      = "curious",      "Curious"
        FUNNY        = "funny",        "Funny"

    campaign         = models.ForeignKey(EmailCampaign, on_delete=models.CASCADE, related_name="subject_lines")
    text             = models.CharField(max_length=150, help_text="The subject line text (max 150 chars)")
    tone             = models.CharField(max_length=15, choices=Tone.choices, default=Tone.PROFESSIONAL)
    is_ai_generated  = models.BooleanField(default=False, db_index=True)
    performance_score = models.PositiveSmallIntegerField(
        null=True, blank=True,
        help_text="Optional score 0-100 from A/B testing"
    )
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        verbose_name = "Subject Line"
        ordering = ["-created_at"]

    def __str__(self):
        tag = "🤖" if self.is_ai_generated else "✍"
        return f"{tag} {self.text[:60]}"


