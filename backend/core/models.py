class EmailCampaign(models.Model):
    """
    Represents a marketing campaign a user is running.
    Example: "Black Friday Sale", "SaaS Product Launch Q1".
    """

    class Status(models.TextChoices):
        DRAFT    = "draft",    "Draft"
        ACTIVE   = "active",   "Active"
        ARCHIVED = "archived", "Archived"

    class Industry(models.TextChoices):
        ECOMMERCE  = "ecommerce",   "E-commerce"
        SAAS       = "saas",        "SaaS"
        FINANCE    = "finance",     "Finance"
        HEALTHCARE = "healthcare",  "Healthcare"
        EDUCATION  = "education",   "Education"
        OTHER      = "other",       "Other"

    owner           = models.ForeignKey(User, on_delete=models.CASCADE, related_name="campaigns")
    title           = models.CharField(max_length=120)
    description     = models.TextField(blank=True, help_text="What is this campaign about?")
    industry        = models.CharField(max_length=20, choices=Industry.choices, default=Industry.OTHER)
    target_audience = models.CharField(max_length=200, blank=True, help_text="e.g. Women 25-40 interested in fashion")
    status          = models.CharField(max_length=10, choices=Status.choices, default=Status.DRAFT, db_index=True)
    created_at      = models.DateTimeField(auto_now_add=True)
    updated_at      = models.DateTimeField(auto_now=True)

    class Meta:
        verbose_name = "Email Campaign"
        ordering = ["-created_at"]

    def __str__(self):
        return f"{self.title} ({self.owner.email})"


