class ChatSession(models.Model):
    """
    A conversation thread between a user and the AI.
    Optionally linked to a specific campaign for context.
    """

    user     = models.ForeignKey(User, on_delete=models.CASCADE, related_name="chat_sessions")
    campaign = models.ForeignKey(
        EmailCampaign,
        on_delete=models.SET_NULL,
        null=True, blank=True,
        related_name="chat_sessions",
        help_text="Optional: link this session to a campaign"
    )
    title      = models.CharField(max_length=120, blank=True, help_text="Auto-generated or user-defined title")
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        verbose_name = "Chat Session"
        ordering = ["-created_at"]

    def __str__(self):
        return f"Session '{self.title or self.id}' — {self.user.email}"


class ChatMessage(models.Model):
    """
    A single message inside a ChatSession.
    role = 'user'      → sent by the human
    role = 'assistant' → AI response (stored as Markdown)
    """

    class Role(models.TextChoices):
        USER      = "user",      "User"
        ASSISTANT = "assistant", "Assistant"

    session    = models.ForeignKey(ChatSession, on_delete=models.CASCADE, related_name="messages")
    role       = models.CharField(max_length=10, choices=Role.choices)
    content    = models.TextField()
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        verbose_name = "Chat Message"
        ordering = ["created_at"]  # chronological order

    def __str__(self):
        preview = self.content[:50]
        return f"[{self.role.upper()}] {preview}"
