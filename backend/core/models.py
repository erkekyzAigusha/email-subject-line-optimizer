# USER
# ══════════════════════════════════════════════════════════════

class UserManager(BaseUserManager):
    """Custom manager so we can create users with email instead of username."""

    def create_user(self, email, password=None, **extra_fields):
        if not email:
            raise ValueError("Email is required.")
        email = self.normalize_email(email)
        user = self.model(email=email, **extra_fields)
        user.set_password(password)
        user.save(using=self._db)
        return user

    def create_superuser(self, email, password=None, **extra_fields):
        extra_fields.setdefault("is_staff", True)
        extra_fields.setdefault("is_superuser", True)
        extra_fields.setdefault("is_admin", True)
        return self.create_user(email, password, **extra_fields)


class User(AbstractBaseUser, PermissionsMixin):
    """
    Custom user model.
    - email is the login field (no username).
    - Roles: is_admin (staff) / is_user (regular).
    - is_blocked lets admins lock out a user without deleting them.
    """

    email      = models.EmailField(unique=True)
    first_name = models.CharField(max_length=50, blank=True)
    last_name  = models.CharField(max_length=50, blank=True)
    avatar     = models.ImageField(upload_to="avatars/", null=True, blank=True)

    # Roles
    is_admin = models.BooleanField(default=False)  # admin dashboard access
    is_user  = models.BooleanField(default=True)   # regular user

    # Status
    is_active  = models.BooleanField(default=True)
    is_blocked = models.BooleanField(default=False, db_index=True)  # admin can block
    is_staff   = models.BooleanField(default=False)  # Django admin access

    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    objects = UserManager()

    USERNAME_FIELD  = "email"
    REQUIRED_FIELDS = []  # email + password are enough

    class Meta:
        verbose_name = "User"
        ordering = ["-created_at"]

    def __str__(self):
        return self.email

    @property
    def full_name(self):
        return f"{self.first_name} {self.last_name}".strip() or self.email


